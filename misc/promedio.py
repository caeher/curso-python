def calculate_average(numbers):
    """
    Calcula el promedio de una lista de números

    Parámetros:
    numbers (list): lista de números

    Retorna:
    float: promedio de los números
    
    """
    return sum(numbers) / len(numbers)

numbers = [1, 2, 3, 4, 5]
print(calculate_average(numbers))

