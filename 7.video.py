print("Uso de separadores en la función print()")
print("Hola", "Mundo", sep="-")
print("Hola", "Mundo", sep="***")

print("Uso de terminadores en la función print()")
print("Hola", end=" ")
print("Mundo")
print("Hola", end="***")
print("Mundo")

print("En la función print() se pueden utilizar los parámetros sep y end para personalizar el separador entre los argumentos y el terminador al final de la impresión, respectivamente")
